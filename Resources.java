public class Resources {
    private String desktop;
    private String chair;

    public Resources(String desktop, String chair) {
        this.desktop = desktop;
        this.chair = chair;
    }

    public String getDesktop() { return desktop; }
    public void setDesktop(String desktop) { this.desktop = desktop; }

    public String getChair() { return chair; }
    public void setChair(String chair) { this.chair = chair; }
}